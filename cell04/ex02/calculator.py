#!/usr/bin/env python3


first_str = input("Give me the first number: ")
second_str = input("Give me the second number: ")

num1 = int(first_str)
num2 = int(second_str)

print("Thank you!")

print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} / {num2} = {num1 // num2}")
print(f"{num1} * {num2} = {num1 * num2}")
